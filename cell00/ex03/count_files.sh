find . -maxdepth 1 -type f -o -type d | wc -l
#find .
#ใช้คำสั่ง find เพื่อค้นหาไฟล์และโฟลเดอร์
#จุด (.) หมายถึงให้ค้นหาในไดเรกทอรีปัจจุบัน

#-maxdepth 1
#กำหนดความลึกในการค้นหาเป็น 1
#หมายความว่าจะค้นหาเฉพาะในไดเรกทอรีปัจจุบันเท่านั้น โดยไม่ลงลึกไปในโฟลเดอร์ย่อย
#ถ้าไม่ใช้ตัวเลือกนี้ คำสั่ง find จะค้นหาทั้งหมดที่อยู่ภายใต้ไดเรกทอรีนั้น (รวมถึงไดเรกทอรีย่อย)

#-type f
#ตัวเลือกนี้บอกให้ค้นหาเฉพาะ ไฟล์ปกติ (regular files)

#-o
#ย่อมาจาก "or" (หรือ)
#ใช้เพื่อเชื่อมต่อเงื่อนไขการค้นหาหลายๆ แบบ
#ในที่นี้ใช้เพื่อบอกว่า "ค้นหาไฟล์ปกติ หรือ ไดเรกทอรี"

#-type d
#ตัวเลือกนี้บอกให้ค้นหาเฉพาะ ไดเรกทอรี (directories)

#| (Pipe)
#ใช้เพื่อส่งผลลัพธ์จากคำสั่งก่อนหน้าไปยังคำสั่งถัดไป
#ในที่นี้คือส่งรายการไฟล์และโฟลเดอร์ไปยังคำสั่ง wc -l

#wc -l
#wc ย่อมาจาก "word count" (นับคำ)
#ตัวเลือก -l (lowercase L) ใช้เพื่อนับจำนวนบรรทัด
#ในที่นี้ใช้เพื่อนับจำนวนรายการไฟล์และโฟลเดอร์ที่ได้จากคำสั่ง find